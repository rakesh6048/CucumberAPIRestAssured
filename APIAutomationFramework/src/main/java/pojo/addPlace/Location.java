package pojo.addPlace;

public class Location {
	
	private double lat;
	private double lng;
	public double getLat() {
		return lat;
	}
	public double setLat(double lat) {
		return this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double d) {
		this.lng = d;
	}
	
	

}
